    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String no = "",
                nama = "",
                nik = "",
                tgl_lahir = "",
                alamat = "",
                jenis_kelamin = "",
                no_hp = "";
        try {
            no = txtNo.getText();
            nama = txtNama.getText();
            nik = txtNik.getText();
            tgl_lahir = jDateTglLahir.getDateFormatString();
            alamat = txtAlamat.getText();
            jenis_kelamin = (String) cmbJK.getSelectedItem();
            no_hp = txtNoHp.getText();
            Connection conn = koneksi.getConnection();
            Statement stmt = conn.createStatement();
            String sql = "insert into pasien value ('"+no
                    +"','"+nama
                    +"','"+nik
                    +"','"+tanggal_lahir
                    +"','"+alamat
                    +"','"+jenis_kelamin
                    +"','"+no_hp+"')";
            stmt.execute(sql);
            TampilData();
            hapus();
        } catch (SQLException ex) {
            Logger.getLogger(form_pasien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private String tanggal_lahir;
    public void tanggal(){
        if(jDateTglLahir.getDate() !=null) {
            String pattern = "dd MMMM yyyy";
            SimpleDateFormat format = new SimpleDateFormat(pattern);
            tanggal_lahir = String.valueOf(format.format(jDateTglLahir.getDate()));
        }
    }
    
    public void lahir() throws SQLException{
        try {
            Connection conn = koneksi.getConnection();
            Statement stmt = conn.createStatement();
            String sql = "select * from pasien where nik='" + txtNik.getText()+"'";
            java.sql.ResultSet r = stmt.executeQuery(sql);
            while (r.next()) {
                jDateTglLahir.setDate(r.getDate("tgl_lahir"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(form_pasien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
        
    public void hapus(){
        txtNo.setText("");
        txtNama.setText("");
        txtNik.setText("");
        
        txtAlamat.setText("");
        cmbJK.setSelectedItem("Laki-Laki");
        txtNoHp.setText("");
    }
    private void jDateTglLahirPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jDateTglLahirPropertyChange
        tanggal();
    }//GEN-LAST:event_jDateTglLahirPropertyChange

    private void tblPasienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPasienMouseClicked
        if (evt.getClickCount() == 1) {
            try {
                txtNo.setText(String.valueOf(tblPasien.getValueAt(tblPasien.getSelectedRow(),0)));
                txtNama.setText(String.valueOf(tblPasien.getValueAt(tblPasien.getSelectedRow(),1)));
                txtNik.setText(String.valueOf(tblPasien.getValueAt(tblPasien.getSelectedRow(),2)));
                lahir();
                txtAlamat.setText(String.valueOf(tblPasien.getValueAt(tblPasien.getSelectedRow(),4)));
                cmbJK.setSelectedItem(String.valueOf(tblPasien.getValueAt(tblPasien.getSelectedRow(),5)));
                txtNoHp.setText(String.valueOf(tblPasien.getValueAt(tblPasien.getSelectedRow(),6)));
            } catch (SQLException ex) {
                Logger.getLogger(form_pasien.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tblPasienMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            Connection conn = koneksi.getConnection();
            Statement stmt = conn.createStatement();
            String Sql = "update pasien set no='"+txtNo.getText()
                    +"',nama= '"+txtNama.getText()
                    +"',tgl_lahir= '"+tanggal_lahir
                    +"',alamat= '"+txtAlamat.getText()
                    +"',jenis_kelamin= '"+cmbJK.getSelectedItem()
                    +"',no_hp= '"+txtNoHp.getText()
                    +"'where nik= '"+txtNik.getText()+"'";
            stmt.execute(Sql);
            TampilData();
            hapus();
            
        } catch (SQLException ex) {
            Logger.getLogger(form_pasien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

