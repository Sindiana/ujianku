    public void TampilData() {
        DftTblModel_pasien = new DefaultTableModel();
        DftTblModel_pasien.addColumn("No.");
        DftTblModel_pasien.addColumn("Nama Pasien");
        DftTblModel_pasien.addColumn("NIK");
        DftTblModel_pasien.addColumn("Tanggal Lahir");
        DftTblModel_pasien.addColumn("Alamat");
        DftTblModel_pasien.addColumn("Jenis Kelamin");
        DftTblModel_pasien.addColumn("No. Handphone");
        tblPasien.setModel(DftTblModel_pasien);
        Connection conn = koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from pasien";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_pasien.addRow(new Object[]{
                    res.getString("no"),
                    res.getString("nama"),
                    res.getString("nik"),
                    res.getString("tgl_lahir"),
                    res.getString("alamat"),
                    res.getString("jenis_kelamin"),
                    res.getString("no_hp")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

